    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
         MysqlDataSource m=new MysqlDataSource();
        m.setUser("root");
        m.setPassword("mysql");
        m.setDatabaseName("criminal_investigator");
        m.setServerName("localhost");
        m.setPortNumber(3306);
        try
        {
            Connection con=m.getConnection();
            PreparedStatement ps=con.prepareStatement("Select case_name,suspect_name,rank,image from case_detail,suspect_details where rank in(Select max(rank) from suspect_details where case_id=?) and case_detail.case_id=?");
            ps.setInt(1,Integer.parseInt((String)jComboBox1.getSelectedItem()));
            ps.setInt(2,Integer.parseInt((String)jComboBox1.getSelectedItem()));
            ResultSet rs=ps.executeQuery();
            while(rs.next())
            {
                jTextField1.setText(rs.getString("case_name"));
                jTextField2.setText(rs.getString("suspect_name"));
                jTextField3.setText(Float.toString(rs.getFloat("rank")));
                String file=System.getProperty("user.dir")+"\\src\\profile_pics\\suspects\\"+rs.getString("image");
                File f=new File(file);
                BufferedImage img=ImageIO.read(f);
                Image dimg = img.getScaledInstance(jLabel8.getWidth(), jLabel8.getHeight(),
        Image.SCALE_SMOOTH);
                ImageIcon icon=new ImageIcon(dimg);
                jLabel8.setIcon(icon);
            }
        }
        catch(Exception ex)
        {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

